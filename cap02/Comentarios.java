package cap02;

/**
 * Exemplos de comentários Essa classe demonstra a utilização de variáveis em
 * uma classe Java são declaradas variáveis dos tipos int e double O exemplo
 * também imprime os valores das variáveis
 */
public class Comentarios {

    public static void main(String[] args) {
        int matricula = 1089, idade = 20; //declaração de variáveis do tipo inteiro
        double dolar = 2.62;
        /*
        impressão das variáveis na tela
         */
        System.out.printf("Matricula: %d idade: %d dolar: %.2f%n", matricula, idade, dolar);

    }

}
